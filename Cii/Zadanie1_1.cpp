#include <iostream> // Подключение библиотеки для работы с вводом/выводом (std::cout, std::endl)
#include <locale>   // Подключение библиотеки для поддержки русской локализации

using namespace std; // Использование пространства имен std для удобства работы с cout и другими элементами

// Определение функции main с именем main1, чтобы избежать конфликта с возможной другой функцией main
void main1(void)
{
    setlocale(LC_ALL, "Russian"); // Устанавливаем русскую локаль для корректного отображения кириллицы в консоли

    int i, k; // Объявляем две целочисленные переменные i и k

    int power(int, int); // Объявляем функцию power(), которая принимает два целочисленных параметра (основание и степень)

    // Цикл от 0 до 9 (всего 10 итераций)
    for (i = 0; i < 10; i++)
    {
        k = power(2, i); // Вызываем функцию power(2, i) для вычисления степени и сохраняем результат в k
        std::cout << "i = " << i << ", k = " << k << endl; // Выводим значения i и k
    }
}

// Определение функции power, которая возводит число x в степень n
int power(int x, int n)
{
    int i, p = 1; // Объявляем переменную i для счетчика и p = 1 (инициализируем результат)

    // Цикл для возведения в степень
    for (i = 1; i < n; i++)
        p *= x; // Умножаем p на x (p = p * x)

    return p; // Возвращаем результат
}
